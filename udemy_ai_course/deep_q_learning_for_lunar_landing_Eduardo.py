"""## Part 1 - Building the AI

### Creating the architecture of the Neural Network
"""

class Network(nn.Module):

  def __init__(self, state_size, action_size, seed = 42) -> None:
    # activate inheritance
    super(Network, self).__init__()
    # activate seed
    self.seed = torch.manual_seed(seed)

    #architecture
    self.fc1 = nn.Linear(state_size, 64)  # 64 is number of fully connected
                                          # neurons on the first layer. 64
                                          # was reached after experimentation
    self.fc2 = nn.Linear(64, 64) # 2 layers!
    self.fc3 = nn.Linear(64, action_size) # output layer
    #

  def forward(self, state):
    x = self.fc1(state)
    x = F.relu(x)  # F is torch.nn.functional
                   # relu() rectifier activation function
    x = self.fc2(x)
    x = F.relu(x)
    return self.fc3(x)

